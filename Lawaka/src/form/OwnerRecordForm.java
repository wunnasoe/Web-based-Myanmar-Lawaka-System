package form;


public class OwnerRecordForm {
	String serial_no;
	String name;
	String nrc_no;
	String date_of_birth;
	String nationality;
	String region;
	String type_of_law;
	String useful_law;
	String gender;
	String file_no;
	String issued_date;
	String remark;
	public OwnerRecordForm(){}
	public String getSerial_no() {
		return serial_no;
	}
	public void setSerial_no(String serial_no) {
		this.serial_no = serial_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNrc_no() {
		return nrc_no;
	}
	public void setNrc_no(String nrc_no) {
		this.nrc_no = nrc_no;
	}
	public String getDate_of_birth() {
		return date_of_birth;
	}
	public void setDate_of_birth(String date_of_birth) {
		this.date_of_birth = date_of_birth;
	}
	public String getNationality() {
		return nationality;
	}
	public void setNationality(String nationality) {
		this.nationality = nationality;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getType_of_law() {
		return type_of_law;
	}
	public void setType_of_law(String type_of_law) {
		this.type_of_law = type_of_law;
	}
	public String getUseful_law() {
		return useful_law;
	}
	public void setUseful_law(String useful_law) {
		this.useful_law = useful_law;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getFile_no() {
		return file_no;
	}
	public void setFile_no(String file_no) {
		this.file_no = file_no;
	}	
	public String getIssued_date() {
		return issued_date;
	}
	public void setIssued_date(String issued_date) {
		this.issued_date = issued_date;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	

}
